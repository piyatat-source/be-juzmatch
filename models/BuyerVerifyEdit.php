        if (!CheckNumber($this->installment_min->FormValue)) {
            $this->installment_min->addErrorMessage($this->installment_min->getErrorMessage(false));
        }
        if ($this->installment_max->Required) {
            if (!$this->installment_max->IsDetailKey && EmptyValue($this->installment_max->FormValue)) {
                $this->installment_max->addErrorMessage(str_replace("%s", $this->installment_max->caption(), $this->installment_max->RequiredErrorMessage));
            }
        }
        if (!CheckNumber($this->installment_max->FormValue)) {
            $this->installment_max->addErrorMessage($this->installment_max->getErrorMessage(false));
        }
        if ($this->num_bedroom->Required) {
            if ($this->num_bedroom->FormValue == "") {
                $this->num_bedroom->addErrorMessage(str_replace("%s", $this->num_bedroom->caption(), $this->num_bedroom->RequiredErrorMessage));
            }
        }
        if ($this->latitude->Required) {
            if (!$this->latitude->IsDetailKey && EmptyValue($this->latitude->FormValue)) {
                $this->latitude->addErrorMessage(str_replace("%s", $this->latitude->caption(), $this->latitude->RequiredErrorMessage));
            }
        }
        if ($this->longitude->Required) {
            if (!$this->longitude->IsDetailKey && EmptyValue($this->longitude->FormValue)) {
                $this->longitude->addErrorMessage(str_replace("%s", $this->longitude->caption(), $this->longitude->RequiredErrorMessage));
            }
        }
        if ($this->uip->Required) {
            if (!$this->uip->IsDetailKey && EmptyValue($this->uip->FormValue)) {
                $this->uip->addErrorMessage(str_replace("%s", $this->uip->caption(), $this->uip->RequiredErrorMessage));
